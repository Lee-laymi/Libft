#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*a;

	while (s[i] == c && i < n)
	{
		a = ((unsigned char *)s);
		n--;
	}
	return (NULL);
}
